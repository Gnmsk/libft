#include "libft.h"

int		ft_strncmp(char *s1, char *s2, int len)
{
	int i;

	i = 0;
	if (len == 0)
		return (0);
	while (s1[i] != '\0' && s2[i] != '\0' && i < len)
	{
		if (s1[i] != s2[i])
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
		i++;
	}
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
